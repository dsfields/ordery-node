'use strict';

/**
 * An enumerated value of sort directions.
 *
 * @enum
 * @readonly
 */
const Direction = {

  /**
   * Ascending order.
   * @readonly
   */
  ASC: 'asc',

  /**
   * Descending order.
   * @readonly
   */
  DESC: 'desc',
};


Object.freeze(Direction);


module.exports = Direction;
